// Logical operator 

// AND  ---   && 
// true  &&  true  =====> true
// false &&  true  =====> false
// true  &&  false =====> false
// false &&  false =====> false




// OR   ---   ||

// true  ||  true  =====> true
// false ||  true  =====> true
// true  ||  false =====> true
// false ||  false =====> false

console.log(3 == 3 || 8 === 8)
console.log(3 === '3' || 8 === 8)
console.log(3 !== '3' || 8 !== 8)
console.log(3 === '3' || 8 !== 8)


// Not   ---  !

console.log(!(1 == 1))
console.log(!(1 == 2))
















